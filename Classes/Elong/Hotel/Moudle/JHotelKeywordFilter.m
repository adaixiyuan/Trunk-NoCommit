#import "JHotelKeywordFilter.h"

@implementation JHotelKeywordFilter

- (void) dealloc{
    self.keyword = nil;
    [super dealloc];
}

- (void)copyDataFrom:(JHotelKeywordFilter *)dataFrom{
    self.keywordType = dataFrom.keywordType;
    self.keyword = dataFrom.keyword;
    self.poi = dataFrom.poi;
    self.lat = dataFrom.lat;
    self.lng = dataFrom.lng;
    self.pid = dataFrom.pid;
}
@end
